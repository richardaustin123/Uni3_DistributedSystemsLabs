#include <iostream>
using namespace std;

void printLines() {
    for (int i = 1; i < 13; i++) {
        cout << "--- ";
    }
}

void runTimesTable() {
    cout << "  | ";
    for (int i = 1; i < 13; i++) {
        cout << i << " | ";
    }

    cout << "\n";
    printLines();
    cout << "\n";

    for (int i = 1; i < 13; i++) {
        cout << i << " | ";
        for (int j = 1; j < 13; j++) {
            cout << i * j << " | ";
            if (j == 12) {
                cout << "\n";
                printLines();
                cout << "\n";
            }
        }
    }
}

int main () {
    runTimesTable();
}

